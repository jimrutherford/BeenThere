#import "UIColor+Styles.h"

@implementation UIColor(Styles)

+(UIColor*) btMediumBlueColor
{
    return [UIColor colorWithRed:0.05 green:0.63 blue:1 alpha:1];
}

+(UIColor*) btDarkBlueColor
{
    return [UIColor colorWithRed:0 green:0.31 blue:0.5 alpha:1];
}

+(UIColor*) btDarkRedColor
{
    return [UIColor colorWithRed:0.7 green:0.07 blue:0.07 alpha:1];
}

+(UIColor*) btDeepRedColor
{
    return [UIColor colorWithRed:0.4 green:0 blue:0 alpha:1];
}

@end
